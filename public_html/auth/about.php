<h1>What is the SSTS?</h1>
<p>SSTS stands for the Simulated Stock Trading System. It was developed in the Fall of 2014 for a final project in CS 324: Principles of Software 
Engineering. This project was created by team UG-2 which consisted of John Pigott, Michael Mastantuono, Rafael Torres-Collazo, and Megan Rodriguez. 
The system had several requirements that every team in the course had to follow including a what-if scenario generator, real-time stock data from 
the NASDAQ and Dow-Jones stock markets, etc.</p>
